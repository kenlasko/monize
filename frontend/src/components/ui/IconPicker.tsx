const ICON_NAMES = Object.keys(ICON_DEFINITIONS);

export function IconPicker({ value, onChange, label }: IconPickerProps) {
  const [isOpen, setIsOpen] = useState(false);

  const selectedIcon = value && ICON_DEFINITIONS[value] ? value : 'chart-bar';

  return (
    <div className="relative">
      {label && (
        <label className="block text-sm font-medium text-gray-700 dark:text-gray-300 mb-1">
          {label}
        </label>
      )}
      <button
        type="button"
        onClick={() => setIsOpen(!isOpen)}
        className="w-full flex items-center justify-between px-3 py-2 border border-gray-300 dark:border-gray-600 rounded-md shadow-sm bg-white dark:bg-gray-800 text-gray-900 dark:text-gray-100 hover:bg-gray-50 dark:hover:bg-gray-700 focus:outline-none focus:ring-2 focus:ring-blue-500"
      >
        <span className="text-gray-700 dark:text-gray-200">
          {ICON_DEFINITIONS[selectedIcon]}
        </span>
        <svg
          className={`w-5 h-5 text-gray-400 transition-transform ${isOpen ? 'rotate-180' : ''}`}
          fill="none"
          viewBox="0 0 24 24"
          stroke="currentColor"
        >
          <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M19 9l-7 7-7-7" />
        </svg>
      </button>

      {isOpen && (
        <>
          <div
            className="fixed inset-0 z-10"
            onClick={() => setIsOpen(false)}
          />
          <div className="absolute z-20 mt-1 w-full bg-white dark:bg-gray-800 border border-gray-300 dark:border-gray-600 rounded-md shadow-lg p-2 max-h-64 overflow-y-auto">
            <div className="grid grid-cols-6 gap-1">
              {ICON_NAMES.map((iconName) => (
                <button
                  key={iconName}
                  type="button"
                  onClick={() => {
                    onChange(iconName);
                    setIsOpen(false);
                  }}
                  className={`p-2 rounded hover:bg-gray-100 dark:hover:bg-gray-700 flex items-center justify-center text-gray-700 dark:text-gray-200 ${
                    selectedIcon === iconName ? 'bg-blue-100 dark:bg-blue-900' : ''
                  }`}
                  title={iconName.replace(/-/g, ' ')}
                >
                  {ICON_DEFINITIONS[iconName]}
                </button>
              ))}
            </div>
          </div>
        </>
      )}
    </div>
  );
}

// Export for use in other components (rendering saved icons)
export function getIconComponent(iconName: string): React.ReactNode {
  return ICON_DEFINITIONS[iconName] || ICON_DEFINITIONS['chart-bar'];
}
